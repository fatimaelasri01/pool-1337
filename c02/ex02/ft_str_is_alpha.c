int	ft_str_is_alpha(char *str)
{
	char	*p;

	p = str;
	while (*p && ((*p >= 'a' && *p <= 'z') || (*p >= 'A' && *p <= 'Z')))
	{
		p++;
	}
	if (*p == '\0')
		return (1);
	else
		return (0);
}
