int	ft_atoi(char *str)
{
	int	nb;
	int	s;

	nb = 0;
	s = 1;
	while (*str && (*str == ' ' || (*str >= 9 && *str <= 13)))
		str++;
	while (*str && (*str == '-' || *str == '+'))
	{
		if (*str == '-')
			s = s * (-1);
		str++;
	}
	while (*str && (*str >= '0' && *str <= '9'))
	{
		nb = (nb * 10) + (*str - '0');
		str++;
	}
	return (nb * s);
}
